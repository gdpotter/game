        getTile: function(x, y) {
            var chunkX = Math.floor(x / 16);
            var chunkY = Math.floor(y / 16);

            var tileX = x - (chunkX * 16);
            var tileY = y - (chunkY * 16);

            var tilePosition = (tileX + (16 * tileY));

            var chunk = this.getChunk(chunkX, chunkY);

            if (chunk) {
                return chunk[tilePosition];
            } else {
                return -1;
            }

        },

        getChunk: function(x, y) {
            if (!this.chunks[x + ',' + y]) {
                this.generateChunk(x, y);
            }
            return this.chunks[x + ',' + y];
        },

        generateChunk: function(chunkX, chunkY) {
            console.log('Generating ' + chunkX + ', ' + chunkY);
            var heights = [];
            var i = 0;
            var chunk = [];

            var noise = new SimplexNoise({
                random: new Alea(this.seed)
            });

            for (i=0; i < 16; i++) {
                heights[i] = Math.round(noise.noise(i/16 + chunkX, 0) * 4);
            }

            var x;
            var y = chunkY * 16;
            for (i=0; i < 256; i++) {
                x = i % 16;
                if (x === 0) {
                    y++;
                }
                if (y < heights[x]) {
                    chunk[i] = 0;
                } else if (y == heights[x]) {
                    chunk[i] = 2;
                } else {
                    chunk[i] = 1;
                }
            }

            this.chunks[chunkX + ',' + chunkY] = chunk;
        }
